// Using Push Method

let colors = ["Red", "Green", "Blue"];

// Add an items to the end of the array(Push)
colors.push("Pink", "Voilet");

// Add an items from begineer of the array(Unshift)
colors.unshift("Yellow", "Grey");
 
// print output
console.log(colors); // Prints: Yellow,Grey,Red,Green,Blue,Pink,Voilet
console.log(colors.length); // Prints: 7

